magnit = {"молоко", "соль", "сахар"}
pyaterochka = {"мясо", "молоко", "сыр"}

отсутствуют_в_пятерочке = magnit - pyaterochka
print(отсутствуют_в_пятерочке)  # {'соль', 'сахар'}

отсутствуют_в_магните = pyaterochka - magnit
print(отсутствуют_в_магните)  # {'мясо', 'сыр'}

все_товары = magnit | pyaterochka
print(все_товары)  # {'молоко', 'соль', 'сахар', 'мясо', 'сыр'}

равны = magnit == pyaterochka
print(равны)  # False
